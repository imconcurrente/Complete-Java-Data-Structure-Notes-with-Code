package com.java.strings;

public class Ascii {
    public static void main(String[] args) {
        // how to get the Ascii value
        char ch = 'a';
        System.out.println(ch + 0); // for Ascii value
        System.out.println((char)(ch + 0)); // prints a  // for character
        System.out.println((char)(ch + 1)); // prints b like this so onn

    }
}
